public class integer{
    public static void main(String[] args){
        int age;
        age = 13;
        // int age = 13;
        System.out.println(age);

        age = 13 - 11;
        System.out.println(age);

        age = 13 + 11;
        System.out.println(age);

        age = 13 / 1;
        System.out.println(age);

        age = 13 * 11;
        System.out.println(age);
    }
}